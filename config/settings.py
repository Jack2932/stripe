def stripe_pair(currency_code: str) -> dict[str, str]:
    suffix = currency_code.upper()
    return {
        "public": env(f"STRIPE_PUBLIC_KEY_{suffix}", env("STRIPE_PUBLIC_KEY", "")) or "",
        "secret": env(f"STRIPE_SECRET_KEY_{suffix}", env("STRIPE_SECRET_KEY", "")) or "",
    }


